SELECT 
    INV.DATE_ AS [TARİH], 
    ISNULL(LGUSER.NAME, '') AS [KULLANICI_ADI],
    CASE INV.TRCODE 
        WHEN 8 THEN 'Toptan Satış Faturası' 
        WHEN 9 THEN 'Verilen Hizmet Faturası' 
        WHEN 6 THEN 'Satınalma İade Faturası' 
        WHEN 13 THEN 'Satınalma Fiyat Farkı Faturası' 
        WHEN 14 THEN 'Satış Fiyat Farkı Faturası' 
        ELSE 'Diğer' END AS [FATURA_TÜRÜ],
    INV.FICHENO AS [FATURA_NO], 
    CLD.DEFINITION_ AS [CARİ_UNVAN],
    STL.VAT AS [KDV_ORANI], 
    STL.VATAMNT AS [ÖN_MUH_KDV_TUTARI], 
    -- Muhasebe tarafında alacak bakiyesi (-) geleceği için tutarı ters çeviriyoruz (ABS veya önüne - koyarak)
    ISNULL(EML.MUH_KDV * -1, 0) AS [GENEL_MUH_KDV_TUTARI],
    ISNULL(EML.ACCOUNTCODE, 'HESAP BULUNAMADI') AS [MUH_HESAP_KODU]
FROM LG_{FIRMNR}_01_STLINE STL WITH(NOLOCK)
JOIN LG_{FIRMNR}_01_INVOICE INV WITH(NOLOCK) ON INV.LOGICALREF = STL.INVOICEREF
LEFT JOIN LG_{FIRMNR}_CLCARD CLD WITH(NOLOCK) ON INV.CLIENTREF = CLD.LOGICALREF
LEFT JOIN L_CAPIUSER LGUSER ON INV.CAPIBLOCK_CREATEDBY = LGUSER.NR
LEFT JOIN (
    -- Muhasebe tarafı (391'li hesaplar)
    SELECT 
        ACCFICHEREF, 
        ACCOUNTCODE,
        CASE 
            WHEN ACCOUNTCODE IN ('391.01.001', '391.05.001', '391.02.001', '391.07.001', '391.04.001') THEN 1
            WHEN ACCOUNTCODE IN ('391.01.004', '391.05.004', '391.02.004', '391.07.004', '391.04.004') THEN 10
            WHEN ACCOUNTCODE IN ('391.05.007') THEN 16.0
            WHEN ACCOUNTCODE IN ('391.01.005', '391.05.005', '391.02.005', '391.07.005', '391.04.005','391.05.006') THEN 20
        END AS ORAN,
        SUM(DEBIT - CREDIT) AS MUH_KDV
    FROM LG_{FIRMNR}_01_EMFLINE WITH(NOLOCK)
    WHERE ACCOUNTCODE IN (
        '391.01.001', '391.01.004', '391.01.005',
        '391.05.001', '391.05.004', '391.05.005',
        '391.02.001', '391.02.004', '391.02.005',
        '391.07.001', '391.07.004', '391.07.005',
        '391.04.001', '391.04.004', '391.04.005','391.05.006'
    )
    AND CANCELLED = 0
    GROUP BY ACCFICHEREF, ACCOUNTCODE
) EML ON INV.ACCFICHEREF = EML.ACCFICHEREF AND STL.VAT = EML.ORAN 

WHERE INV.CANCELLED = 0 
  AND STL.CANCELLED = 0
  AND INV.TRCODE IN (8, 9, 6, 13, 14) 
  AND STL.LINETYPE NOT IN (2, 3) 
  AND INV.DATE_ BETWEEN '20260101' AND '20261231'
